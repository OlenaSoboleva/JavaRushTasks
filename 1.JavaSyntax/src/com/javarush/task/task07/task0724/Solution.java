/* 
Семейная перепись
*/

public class Solution {
    public static void main(String[] args) {

        Human grandfather1= new Human("Юра",true,64);
        Human grandfather2= new Human("Миша",true,64);
        Human grandmother1= new Human("Лариса",false,64);
        Human grandmother2= new Human("Валентина",false,62);
        Human father= new Human("Алексей",true,36,grandfather2,grandmother2);
        Human mother= new Human("Елена",false,36,grandfather1,grandmother1);
        Human child1= new Human("Sofia",true,5,father,mother);
        Human child2= new Human("newSecond",false,0,father,mother);
        Human child3= new Human("newSecond",true,0,father,mother);
        System.out.println(grandfather1);
        System.out.println(grandfather2);
        System.out.println(grandmother1);
        System.out.println(grandmother2);
        System.out.println(father);
        System.out.println(mother);
        System.out.println(child1);
        System.out.println(child2);
        System.out.println(child3);

    }

    public static class Human {
        String name;
        boolean sex;
        int age;
        Human father;
        Human mother;

        public Human(String name, boolean sex, int age, Human father, Human mother) {
            this.name = name;
            this.sex = sex;
            this.age = age;
            this.father = father;
            this.mother = mother;
        }

        public Human(String name, boolean sex, int age) {
            this.name = name;
            this.sex = sex;
            this.age = age;
        }



        public String toString() {
            String text = "";
            text += "Имя: " + this.name;
            text += ", пол: " + (this.sex ? "мужской" : "женский");
            text += ", возраст: " + this.age;

            if (this.father != null)
                text += ", отец: " + this.father.name;

            if (this.mother != null)
                text += ", мать: " + this.mother.name;

            return text;
        }
    }
}






















